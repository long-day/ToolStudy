public class SingleResponsibilityTest {
//单一职责原则（Single responsibility principle）
//这个原则有点特殊,举例中会举到,有时我们会不遵循这个原则
    /*
        对象不应承担太多功能，正如一心不能而用，比如太多的工作(种类)会使人崩溃。
        唯有专注才能保证对象的高内聚；唯有唯一，才能保证对象的细粒度。
    */

    //    此原则较为容易理解,就不进行示例了
    
    //    小结
    /*
        1、降低类的功能复杂度
    　　 2、提高系统的可维护性
    　　 3、变更风险低
    */
}
